from typing import Generator

def generate_cube_numbers(end: int) -> Generator[int, None, None]:
    """–ì–µ–Ω–µ—Ä–∞—Ç–æ—Ä –∫—É–±—ñ–≤ —á–∏—Å–µ–ª –≤—ñ–¥ 2, –ø–æ–∫–∏ cube <= end."""

    num = 2
    while True:
        cube = num ** 3
        if cube > end:
            return  # –∑–∞–≤–µ—Ä—à—É—î–º–æ –≥–µ–Ω–µ—Ä–∞—Ç–æ—Ä
        yield cube
        num += 1


from inspect import isgenerator

