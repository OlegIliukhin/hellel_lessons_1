class Counter:

    # –ö–ª–∞—Å —Ü–∏—Ñ—Ä–æ–≤–æ–≥–æ –ª—ñ—á–∏–ª—å–Ω–∏–∫–∞.
    # –ú–æ–∂–µ –∑–±—ñ–ª—å—à—É–≤–∞—Ç–∏—Å—å (step_up) —ñ –∑–º–µ–Ω—à—É–≤–∞—Ç–∏—Å—å (step_down) –≤ –∑–∞–¥–∞–Ω–∏—Ö –º–µ–∂–∞—Ö.

    def __init__(self, current: int = 1, min_value: int = 0, max_value: int = 10) -> None:

        # –Ü–Ω—ñ—Ü—ñ–∞–ª—ñ–∑–∞—Ü—ñ—è –ª—ñ—á–∏–ª—å–Ω–∏–∫–∞:
        # current ‚Äî –ø–æ—Ç–æ—á–Ω–µ –∑–Ω–∞—á–µ–Ω–Ω—è,
        # min_value ‚Äî –º—ñ–Ω—ñ–º–∞–ª—å–Ω–µ –∑–Ω–∞—á–µ–Ω–Ω—è,
        # max_value ‚Äî –º–∞–∫—Å–∏–º–∞–ª—å–Ω–µ –∑–Ω–∞—á–µ–Ω–Ω—è.

        self.current = current
        self.min_value = min_value
        self.max_value = max_value

    def set_current(self, start: int) -> None:
        #–í—Å—Ç–∞–Ω–æ–≤–ª—é—î –ø–æ—á–∞—Ç–∫–æ–≤–µ –∑–Ω–∞—á–µ–Ω–Ω—è –ª—ñ—á–∏–ª—å–Ω–∏–∫–∞
        self.current = start

    def set_max(self, max_max: int) -> None:
        # –º–∞–∫—Å–∏–º–∞–ª—å–Ω–µ –∑–Ω–∞—á–µ–Ω–Ω—è –ª—ñ—á–∏–ª—å–Ω–∏–∫–∞
        self.max_value = max_max

    def set_min(self, min_min: int) -> None:
        # –í—Å—Ç–∞–Ω–æ–≤–ª—é—î –º—ñ–Ω—ñ–º–∞–ª—å–Ω–µ –∑–Ω–∞—á–µ–Ω–Ω—è –ª—ñ—á–∏–ª—å–Ω–∏–∫–∞
        self.min_value = min_min

    def step_up(self) -> None:

        # –ó–±—ñ–ª—å—à—É—î –∑–Ω–∞—á–µ–Ω–Ω—è –ª—ñ—á–∏–ª—å–Ω–∏–∫–∞ –Ω–∞ 1.
        # –Ø–∫—â–æ –¥–æ—Å—è–≥–Ω—É—Ç–æ –º–∞–∫—Å–∏–º—É–º ‚Äî –≤–∏–∫–∏–¥–∞—î –ø–æ–º–∏–ª–∫—É ValueError.
        if self.current < self.max_value:
            self.current += 1
        else:
            raise ValueError('–î–æ—Å—Ç–∏–≥–Ω—É—Ç –º–∞–∫—Å–∏–º—É–º')

    def step_down(self) -> None:

        # –ó–º–µ–Ω—à—É—î –∑–Ω–∞—á–µ–Ω–Ω—è –ª—ñ—á–∏–ª—å–Ω–∏–∫–∞ –Ω–∞ 1.
        # –Ø–∫—â–æ –¥–æ—Å—è–≥–Ω—É—Ç–æ –º—ñ–Ω—ñ–º—É–º ‚Äî –≤–∏–∫–∏–¥–∞—î –ø–æ–º–∏–ª–∫—É ValueError.
        if self.current > self.min_value:
            self.current -= 1
        else:
            raise ValueError('–î–æ—Å—Ç–∏–≥–Ω—É—Ç –º–∏–Ω–∏–º—É–º')

    def get_current(self) -> int:
        # –ü–æ–≤–µ—Ä—Ç–∞—î –ø–æ—Ç–æ—á–Ω–µ –∑–Ω–∞—á–µ–Ω–Ω—è –ª—ñ—á–∏–ª—å–Ω–∏–∫–∞
        return self.current


