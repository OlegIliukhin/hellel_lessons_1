# –ü—ñ—Å–ª—è –æ—á–∏—â–µ–Ω–Ω—è —Ä—è–¥–∫–∞ –≤–∏–∫–ª–∏–∫–∞—î—Ç—å—Å—è –º–µ—Ç–æ–¥ split(), —è–∫–∏–π —Ä–æ–∑–±–∏–≤–∞—î —Ç–µ–∫—Å—Ç –Ω–∞ —Å–ø–∏—Å–æ–∫ —Å–ª—ñ–≤.
